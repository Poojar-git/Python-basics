#Casting refers to converting one data type into another. In Python, this can be done using built-in functions like `int()`, `float()`, `str()`, and `bool()`. For example, you can convert a string to an integer using `int("123")`, or convert a float to a string using `str(3.14)`. Casting is useful when you need to perform operations that require specific data types.
x = int(1)   # x will be 1
y = int(2.8) # y will be 2
z = int("3") # z will be 3
x = float(1)     # x will be 1.0
